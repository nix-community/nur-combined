#!/usr/bin/env bash
# Generate package-lock.json and npmDepsHash for pi packages
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
LOCK_DIR="$SCRIPT_DIR/locks"
HASH_FILE="$SCRIPT_DIR/npm-deps-hashes.nix"

mkdir -p "$LOCK_DIR"

# Package definitions: name version scope
PACKAGES=(
  "pi-mcp-adapter 2.8.0"
  "pi-web-access 0.10.7"
  "pi-hermes-memory 0.7.13"
  "@gotgenes/pi-subagents 11.3.0 gotgenes"
  "@junghanacs/pi-shell-acp 0.7.6 junghanacs"
  "@juicesharp/rpiv-ask-user-question 1.15.0 juicesharp"
  "@juicesharp/rpiv-btw 1.15.0 juicesharp"
)

# Start the nix file
cat > "$HASH_FILE" << 'EOF'
# Auto-generated by generate-hashes.sh
# Contains npmDepsHash for each pi package
{
EOF

for pkg_def in "${PACKAGES[@]}"; do
  read -r name version scope <<< "$pkg_def"

  if [ -n "$scope" ]; then
    full_name="@${scope}/${name#*@*/}"
    tarball_name="${name#*/}"
    fetch_url="https://registry.npmjs.org/${full_name}/-/${tarball_name}-${version}.tgz"
    safe_name=$(echo "$full_name" | sed 's|[@/]|-|g')
  else
    full_name="$name"
    tarball_name="$name"
    fetch_url="https://registry.npmjs.org/${name}/-/${name}-${version}.tgz"
    safe_name="$name"
  fi

  lock_file="$LOCK_DIR/${safe_name}-${version}-package-lock.json"
  tmp_dir=$(mktemp -d)

  echo "Processing $full_name@$version..."

  # Download and extract tarball
  curl -sL "$fetch_url" | tar xz -C "$tmp_dir"

  # Generate package-lock.json
  cd "$tmp_dir/package"
  npm install --package-lock-only --ignore-scripts --legacy-peer-deps 2>/dev/null

  # Copy lock file
  cp package-lock.json "$lock_file"

  # Compute hash
  hash=$(nix run nixpkgs#prefetch-npm-deps -- "$lock_file" 2>/dev/null)

  echo "  $full_name: $hash"

  # Write to nix file
  cat >> "$HASH_FILE" << ENTRY
  "${full_name}" = {
    lockFile = ./locks/${safe_name}-${version}-package-lock.json;
    npmDepsHash = "${hash}";
  };
ENTRY

  # Cleanup
  cd "$SCRIPT_DIR"
  rm -rf "$tmp_dir"
done

# Close the nix file
cat >> "$HASH_FILE" << 'EOF'
}
EOF

echo ""
echo "Done! Hashes written to $HASH_FILE"
echo "Lock files saved to $LOCK_DIR/"

#!/usr/bin/env nix-shell
#! nix-shell -i bash -p curl jq nix

set -euo pipefail

VERSION=$(curl -s https://api.mefrp.com/api/auth/products | jq -r '.data[] | select(.productId == "MEFrp-Core") | .version')
echo "VERSION: $VERSION"

ALIST_API="https://drive.mcsl.com.cn/api/fs/list"
LIST_JSON=$(curl -s -G "$ALIST_API" --data-urlencode "path=/ME-Frp/115Life/MEFrp-Core/$VERSION")

declare -A ARCH_MAP=(
    ["x86_64-linux"]="amd64"
    ["aarch64-linux"]="arm64"
    ["i686-linux"]="386"
    ["riscv64-linux"]="riscv64"
    ["armv7l-linux"]="arm_hf"
    ["loongarch64-linux"]="loong64"
    ["mips64-linux"]="mips64"
    ["mips64el-linux"]="mips64le"
    ["mips-linux"]="mips"
    ["mipsel-linux"]="mipsle"
    ["s390x-linux"]="s390x"
    ["arm-linux"]="arm"
)

to_sri() {
    local hex="$1"
    nix hash to-sri --type sha1 "$hex"
}

cat <<EOF > sources.nix
# This file is generated by update.sh. Do not edit!
{
  version = "$VERSION";
  platforms = {
EOF

for nix_arch in "${!ARCH_MAP[@]}"; do
    core_arch=${ARCH_MAP[$nix_arch]}
    
    # 在 JSON 中查找对应的文件名和哈希
    # 匹配模式：mefrpc_linux_{arch}_{version}.tar
    # 使用 jq 提取对应的 sha1
    TARGET_NAME="mefrpc_linux_${core_arch}_${VERSION}.tar"
    FILE_INFO=$(echo "$LIST_JSON" | jq -r ".data.content[] | select(.name == \"$TARGET_NAME\")")
    
    if [ -n "$FILE_INFO" ]; then
        SHA1=$(echo "$FILE_INFO" | jq -r '.hash_info.sha1')
        HASH=$(to_sri "$SHA1")
        echo "Found $nix_arch ($core_arch): $HASH"
        
        cat <<EOF >> sources.nix
    "$nix_arch" = {
      arch = "$core_arch";
      hash = "$HASH";
    };
EOF
    else
        echo "Warning: No file found for $nix_arch"
    fi
done

cat <<EOF >> sources.nix
  };
}
EOF
